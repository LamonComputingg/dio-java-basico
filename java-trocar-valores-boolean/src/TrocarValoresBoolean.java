public class TrocarValoresBoolean {
    public static void main(String[] args){
        boolean variavel = true;
        variavel = !variavel; //o silan de ! na frente da varavel trocar o valor dela de verdadeiro para falso
        System.out.println(variavel);
        variavel = !variavel;
        System.out.println(variavel); //o silan de ! na frente da varavel trocar o valor dela de verdadeiro para falso
    }
}
